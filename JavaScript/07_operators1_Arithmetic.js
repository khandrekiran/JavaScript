//Arithmetic Operators


let a = 5;
let b = 10;


console.log("a =", a, "& b =", b);
console.log("a + b");
console.log("a + b = ", a + b);
console.log("b - a = ", b - a);
console.log("a * b = ", a * b);
console.log("a / b = ", a / b );
console.log("a % b = ", a % b );        //Modulus
console.log("a ** b = ", a ** b );        //Exponentiation means 5^10




